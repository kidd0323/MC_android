package com.client.entity;

import java.util.ArrayList;

public class BlogPackage {
	
	private int msgNum;
	private ArrayList<String> msgList;
	private ArrayList<String> msgUrlList;
	private String ErrorMessage;
	
	
	public BlogPackage() {
		super();
		ErrorMessage = "empty";
	}


	public BlogPackage(int msgNum, ArrayList<String> msgList,
			ArrayList<String> msgUrlList, String errorMessage) {
		super();
		this.msgNum = msgNum;
		this.msgList = msgList;
		this.msgUrlList = msgUrlList;
		ErrorMessage = errorMessage;
	}


	public int getMsgNum() {
		return msgNum;
	}


	public void setMsgNum(int msgNum) {
		this.msgNum = msgNum;
	}


	public ArrayList<String> getMsgList() {
		return msgList;
	}


	public void setMsgList(ArrayList<String> msgList) {
		this.msgList = msgList;
	}


	public ArrayList<String> getMsgUrlList() {
		return msgUrlList;
	}


	public void setMsgUrlList(ArrayList<String> msgUrlList) {
		this.msgUrlList = msgUrlList;
	}


	public String getErrorMessage() {
		return ErrorMessage;
	}


	public void setErrorMessage(String errorMessage) {
		ErrorMessage = errorMessage;
	}
	

	
}
